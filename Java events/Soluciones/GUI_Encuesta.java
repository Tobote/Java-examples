    //Graba en el fichero los valores de los atributos de la clase
    private void grabarResultados() {
        try {
            escritor = new CsvWriter(new FileWriter(ruta), ';');
            escritor.write(String.valueOf(opc1));
            escritor.write(String.valueOf(opc2));
            escritor.write(String.valueOf(opc3));
            escritor.write(String.valueOf(opc4));
            escritor.flush();
            escritor.endRecord();
        } catch (IOException ex) {
            ex.printStackTrace();
        } finally {
            if (escritor != null) {
                escritor.close();
            }
        }
    }

