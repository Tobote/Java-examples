    //Lee los valores del fichero y los almacena en los atributos de la clase
    public void cargarValores() {
        try {
            lector = new CsvReader(ruta, ';');
            lector.readRecord();
            opc1 = Integer.parseInt(lector.get(0));
            opc2 = Integer.parseInt(lector.get(1));
            opc3 = Integer.parseInt(lector.get(2));
            opc4 = Integer.parseInt(lector.get(3));
            votos=opc1+opc2+opc3+opc4;
        } catch (FileNotFoundException ex) {
            opc1 = opc2 = opc3 = opc4 = 0;
        } catch (IOException ex) {
            ex.printStackTrace();
        } finally {
            if (lector != null) {
                lector.close();
            }
        }
    }

