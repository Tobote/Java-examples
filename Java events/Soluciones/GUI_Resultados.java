    private void mostrarResumen() {
        NumberFormat formateadorNum = NumberFormat.getPercentInstance(); //multiplica por 100 y a침ade %
        formateadorNum.setMaximumFractionDigits(2); //m치ximo 2 decimales
        DateFormat formateadorFech = DateFormat.getDateInstance();
        DateFormat formateadorHora = DateFormat.getTimeInstance();
        double totalVotos = this.ventanaPpal.getVotos();
        double totalOpc1 = (totalVotos == 0) ? 0 : (double) this.ventanaPpal.getOpc1() / totalVotos;
        double totalOpc2 = (totalVotos == 0) ? 0 : (double) this.ventanaPpal.getOpc2() / totalVotos;
        double totalOpc3 = (totalVotos == 0) ? 0 : (double) this.ventanaPpal.getOpc3() / totalVotos;
        double totalOpc4 = (totalVotos == 0) ? 0 : (double) this.ventanaPpal.getOpc4() / totalVotos;
        this.jEditorPane1.setText(
                "<b>Fecha: " + formateadorFech.format(new Date()) + "</b><br>" +
                "<b>Hora: " + formateadorHora.format(new Date()) + "</b><br><br>" +
                "A favor del " + this.ventanaPpal.getjRadioButton1().getText() + " : " + formateadorNum.format(totalOpc1) + "<br>" + 
                "A favor del " + this.ventanaPpal.getjRadioButton2().getText() + " : " + formateadorNum.format(totalOpc2) + "<br>" +  
                "A favor del " + this.ventanaPpal.getjRadioButton3().getText() + " : " + formateadorNum.format(totalOpc3) + "<br>" +  
                "Otra opci칩n : " + this.ventanaPpal.getjRadioButton4().getText() + " : " + formateadorNum.format(totalOpc4)+ "<br><br>" + 
                "<font size=\"14\" color=\"blue\">" + 
                    "<i>Gracias por tu colaboraci칩n!!</i>" + 
                "</font><br>"
        );
    }

