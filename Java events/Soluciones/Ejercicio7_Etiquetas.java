    private void setEtiquetas() {
        this.jPanel1.setBackground(Color.WHITE);
        for (int i = 0; i < 5; i++) {
            etiqueta=new JLabel("Etiqueta" + i);
            etiqueta.setName("Etiqueta" + i); 
            etiqueta.setBorder(BorderFactory.createEmptyBorder(1, 1, 1, 5));
            etiqueta.setFont(new Font("Tahoma",Font.ITALIC, 30));
            this.jPanel1.add(etiqueta);
        }        
    }

