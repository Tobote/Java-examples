    private void calcularRaices() {

        //recogemos los valores de los coeficientes        
        double a = this.ppal.getA();
        double b = this.ppal.getB();
        double c = this.ppal.getC();

        //cambiamos el titulo de la ventana
        String sb = (b >= 0) ? " + " : " ";
        String sc = (c >= 0) ? " + " : " ";
        this.setTitle("Polinomio " + (int) a + "x2" + sb + (int) b + "x" + sc + (int) c);

        //mostramos el resultado
        double discr = b * b - 4 * a * c;

        DecimalFormat df = new DecimalFormat("0.0000");

        if (discr < 0) { //raices imaginarias
            this.jLabel1.setText("Raices imaginarias");
            this.jLabel2.setText("r1= " + ((-1) * b / 2 * a) + "+i" + (Math.sqrt(discr) / 2 * a));
            this.jLabel3.setText("r2= " + ((-1) * b / 2 * a) + "-i" + (Math.sqrt(discr) / 2 * a));
        } else if (discr > 0) {
            this.jLabel1.setText("Raices reales");
            this.jLabel2.setText("r1= " + df.format(((-1) * b + Math.sqrt(discr)) / 2 * a));
            this.jLabel3.setText("r2= " + df.format((((-1) * b - Math.sqrt(discr)) / 2 * a)));
        } else { //discrimiante igual a 0 (raices iguales)
            this.jLabel1.setText("Raices reales iguales");
            this.jLabel2.setText("r= " + ((-1) * b / 2 * a));
            this.jLabel3.setText("");
        }

    }

