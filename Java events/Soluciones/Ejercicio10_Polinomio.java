    private void deshabilitar(AWTEvent evt) {
        ((JTextField) evt.getSource()).setEditable(false);
        ((JTextField) evt.getSource()).setBackground(new Color(204, 204, 204));
    }

    private void habilitar(java.awt.event.InputEvent evt) {
        ((JTextField) evt.getSource()).setEditable(true);
        ((JTextField) evt.getSource()).setBackground(Color.WHITE);
    }

    private boolean esValido(String dato) {
        try {
            int n = Integer.parseInt(dato);
            return true;
        } catch (NumberFormatException ex) {
            return false;
        }
    }

    private void asignarValor(JSlider slider,JTextField caja, Integer coef) {
        if (esValido(caja.getText())) {
            int valor = Integer.parseInt(caja.getText());
            if (valor > MAX) {
                coef = MAX;
            } else if (valor < MIN) {
                coef = MIN;
            } else {
                coef = Integer.parseInt(caja.getText());
            }
            slider.setValue(coef);
            caja.setText(String.valueOf(coef));
        } else {
            caja.setText(String.valueOf(coef));
        }
    }

