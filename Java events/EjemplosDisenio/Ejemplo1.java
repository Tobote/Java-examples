    public void setEtiqueta() {
        try {
            this.LabelTitulo.setText("Mi primer ejemplo");
            this.LabelTitulo.setForeground(Color.RED);
            this.LabelTitulo.setOpaque(true);
            this.LabelTitulo.setBackground(Color.GREEN);//Para cambiar el fondo,hay que cambiar la etiqueta,ya que esta,por defecto,es transparente(linea 28 del codigo)
            this.LabelTitulo.setHorizontalAlignment(JLabel.CENTER);
            //this.LabelTitulo.setFont(new Font("Comic Sans MS",Font.BOLD,20));
            //Font av = Font.createFont(Font.TRUETYPE_FONT, new File(this.getClass().getResource("../Ficheros/Anna.ttf").getPath()));//Da problemas por las carpetas con espacios y acentos
            Font av = Font.createFont(Font.TRUETYPE_FONT, new File("src/Ficheros/Anna.ttf"));
            //Font av2 = Font.createFont(Font.TRUETYPE_FONT, this.getClass().getResourceAsStream("Ficheros/Anna.ttf"));
            av = av.deriveFont(Font.PLAIN, 20);
            this.LabelTitulo.setFont(av);
        } catch (FontFormatException ex) {
            Logger.getLogger(Ejemplo1.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Ejemplo1.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    public void setImagen() {
        this.LabelImagen.setBorder(BorderFactory.createLineBorder(Color.blue, 6));
        
        ImageIcon iconoImagen = new ImageIcon("src/Imagenes/Cebolla.jpg");//No se puede hacer con el getClass por el tema de las carpetas
        Image imagenEscalada = iconoImagen.getImage().getScaledInstance(this.LabelImagen.getWidth(), this.LabelImagen.getHeight(), Image.SCALE_DEFAULT);
        ImageIcon iconoEscalado = new ImageIcon(imagenEscalada);
        this.LabelImagen.setIcon(iconoEscalado);
    }
    
