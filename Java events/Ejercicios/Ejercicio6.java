    private void jTextField2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyPressed
        int aux=Integer.parseInt(this.jTextField2.getText());
        if (evt.getKeyCode() == KeyEvent.VK_F1) {
            if (aux >= 4 && aux< 10) {
                this.jLabel4.setText("Infantil");
            } else if (aux>= 10 && aux < 16) {
                this.jLabel4.setText("Alev√≠n");
            } else if (aux >= 16 && aux < 20) {
                this.jLabel4.setText("Juvenil");
            } else if (aux >= 20) {
                this.jLabel4.setText("Amateur");
            }
        }
    }//GEN-LAST:event_jTextField2KeyPressed

