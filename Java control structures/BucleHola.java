public class BucleHola {
    public static void main(String[] args) {
        
        //mostrar 100 veces "Hola"
        int contador=0;
        
        
        
        while(contador<100){
            System.out.println("Hola="+contador);
            contador++;//contador=contador+1
        }
                
    }
    
}
